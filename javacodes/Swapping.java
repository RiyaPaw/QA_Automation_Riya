package javacodes;

public class Swapping {

	public static void main(String args[]) {
		

	 int a =10;
	 int b =20;
 
	 System.out.println("Before Swap a is " + a + " Before swap b is: " + b);
	 
	 int temp = a;
	 a = b;
	 b =temp;
	  
	 System.out.println("After Swap a is " + a + " After swap b is: " + b);
	
	}
	
}
